#include "libft.h"

int	ft_atoi(char *str)
{
	int	sum;
	int	i;
	int	segno;

	i = 0;
	sum = 0;
	segno = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			segno *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = (sum * 10) + (str[i] - '0');
		i++;
	}
	return (segno * sum);
}

